    public static void main(String[] args) {
        HashMap<Integer, String> h= new HashMap<Integer, String>();
        h.put(100, "Chait");
        h.put(101, "Chait1");
        h.put(102, "Chaiit2");

        for(Map.Entry m: h.entrySet()){
            System.out.println(m.getKey()+" "+ m.getValue());
        }
    }
